import os
import shutil
from sklearn.model_selection import train_test_split

def move_files(files, img_dest, lbl_dest):
    # Ensure destination directories exist
    os.makedirs(img_dest, exist_ok=True)
    os.makedirs(lbl_dest, exist_ok=True)

    for img in files:
        label = img.replace(".jpg", ".txt")

        shutil.copy(os.path.join(image_path, img),
                    os.path.join(img_dest, img))

        shutil.copy(os.path.join(label_path, label),
                    os.path.join(lbl_dest, label))


# Define base path for final dataset
