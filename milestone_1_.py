import os
import cv2
import numpy as np

input_folder = "/content/dataset/train/images"
output_folder = "/content/processed_dataset/images"

os.makedirs(output_folder, exist_ok=True)

img_size = (224,224)

for img in os.listdir(input_folder):

    img_path = os.path.join(input_folder,img)

    image = cv2.imread(img_path)

    if image is None:
        continue

    resized = cv2.resize(image,img_size)

    normalized = resized/255.0

    normalized_img = (normalized*255).astype(np.uint8)

    cv2.imwrite(os.path.join(output_folder,img),normalized_img)

print("Resizing and normalization completed")

